function H = compute_H(x)
    H = zeros(7,8);
    D = (x(1)-x(6))^2 + (x(2)-x(7))^2;
    H(1,1) = 1;
    H(2,2) = 1;
    H(3,3) = 1;
    H(4,4) = 1;
    H(5,:) = [(x(1)-x(6)), (x(2)-x(7)), 0, 0, 0, (x(6)-x(1)), (x(7)-x(2)),0]/sqrt(D);
    H(6,:) = [(x(7)-x(2))/D, (x(1)-x(6))/D, 0, -1, 0, (x(2)-x(7))/D,(x(6)-x(1))/D, 0];
    H(7,:) = [(x(2)-x(7))/D, (x(6)-x(1))/D, 0, 0, 0, (x(7)-x(2))/D,(x(1)-x(6))/D, -1];
end